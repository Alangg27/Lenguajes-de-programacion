float AreaC(float Valor1)
{
    return (Valor1*Valor1) * 3.1416;
}

float AreaP(float Valor1, float Valor2)
{
    return ((Valor1 * Valor2) /2);
}

float AreaT(float Valor1, float Valor2,float Valor3)
{
    return Valor1+Valor2/2*Valor3;
}

float AreaR(float Valor1, float Valor2)
{
    return Valor1 * Valor2;
}

